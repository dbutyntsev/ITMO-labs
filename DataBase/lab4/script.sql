-- Основной код 
EXPLAIN
SELECT Н_ОЦЕНКИ.КОД, Н_ВЕДОМОСТИ.ДАТА
FROM Н_ОЦЕНКИ
INNER JOIN Н_ВЕДОМОСТИ ON Н_ВЕДОМОСТИ.ОЦЕНКА = Н_ОЦЕНКИ.КОД
WHERE Н_ОЦЕНКИ.КОД > '2'
AND Н_ВЕДОМОСТИ.ИД > '1250972'
AND Н_ВЕДОМОСТИ.ИД > '1457443';

